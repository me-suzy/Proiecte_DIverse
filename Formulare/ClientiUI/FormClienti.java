class InputVerifierDate extends InputVerifier{
    public boolean verify(JComponent input){
    if(input instanceof JFormattedTextField){
        JFormattedTextField ftf=(JFormattedTextField)input;
        JFormattedTextField.AbstractFormatter formatter=ftf.getFormatter();
        if(formatter!=null){
            String text=ftf.getText();
            if(text!=null&&!text.equals(""))
                try{
                    Object valNoua=formatter.stringToValue(text);
                    comiteModificari(ftf,valNoua);
                    return true;
                    
                }
                catch(ParseException e){
                    System.out.println("Invalid input");
                    return false;
                }
        }
    }
    return true;
    }
    public void comiteModificari(JFormattedTextField ftf,Object valNoua){
        Clienti objClienti=(Clienti)FormClienti.this.cboClienti.getSelectedItem();
        if(ftf==FormClienti.this.txtCodcl)
            objClienti.setCodcl(new java.math.BigDecimal(((Number)valNoua).doubleValue()));
        else if (ftf==FormClienti.this.txtNumepren)
            objClienti.setNumepren((String)valNoua);
        else if (ftf==FormClienti.this.txtAdresa)
            objClienti.setAdresa((String)valNoua);
        else if (ftf==FormClienti.this.txtEmail)
            objClienti.setEmail((String)valNoua);
        else if (ftf==FormClienti.this.txtTelefon)
            objClienti.setTelefon(new java.math.BigDecimal(((Number)valNoua).doubleValue()));
    }
    public boolean shouldYieldFocus(JComponent input){
        boolean inputValid=this.verify(input);
        if(!inputValid)
        {java.awt.Toolkit.getDefaultToolkit().beep();return false;}
        return true;
        
    }
    
}
