public void anuleazaModificari(Clienti objClient){
     if (objClient.getStare()==Clienti.NOU){  
           FormClienti.this.cboClienti.removeItem(objClient);
    }
    else
           try{objClient.refresh(FormClienti.this.conn);
               ItemListener[] lsnr=cboClienti.getItemListeners();
               lsnr[0].itemStateChanged(new ItemEvent(cboClienti,0,objClient,ItemEvent.SELECTED));
           }
           catch(Exception e){
               Utilitati.tratareErori(e);
           }
}
